#include <stdio.h>
void main()
{
    int i = 10;
    long int li = 10000;
    char a = '*';
    char s[4] = "Riya";
    float f = 10.34234;
    double lf = 293823.234972943;
    printf("\n integer: %d",i);
    printf("\n long integer: %ld", li);
    printf("\n character: %c", a);
    printf("\n string: %s", s);
    printf("\n float: %f", f);
    printf("\n long float: %lf", lf);
}
